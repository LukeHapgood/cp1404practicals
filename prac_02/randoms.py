import random
print(random.randint(5, 20))  # line 1
# Smallest number possible is 5, largest is 20
print(random.randrange(3, 10, 2))  # line 2
# Smallest number possible is 3, largest is 9, 4 is not a possible outcome
print(random.uniform(2.5, 5.5))  # line 3
# Smallest number possible is 2.5, largest is <5.5
print(random.randint(1, 100))
